/* Frame sets are generated by the extraction step — counts must match.
   Phones get square 640px center-crops (frames-m); wide screens get the
   full 1280x720 frames. Chosen once at load. */
const MOBILE =
  matchMedia("(max-width: 720px)").matches ||
  new URLSearchParams(location.search).has("mobile");
const FRAME_DIR = MOBILE ? "frames-m" : "frames";
const CLIPS = Array.from({ length: 12 }, (_, i) => ({
  dir: `${FRAME_DIR}/clip${i + 1}`,
  count: 61,
}));
const DRAW_SCALE = MOBILE ? 0.94 : 0.82;

const N_QUOTES = AFFIRMATIONS.length;
const reducedMotion = matchMedia("(prefers-reduced-motion: reduce)").matches;

