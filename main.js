/* ---------- audio: song chapters follow the affirmations ----------
   Web Audio player (fetch → decode → buffer source): reliable everywhere the
   <audio> element is flaky, and gives sample-accurate chapter seeking. */

const soundToggle = document.getElementById("sound-toggle");
const soundLabel = document.getElementById("sound-label");
const SONG_URL = "audio/song.mp3";

const player = {
  ctx: null,
  gain: null,
  buffer: null,
  source: null,
  startedAt: 0, /* ctx.currentTime when offset 0 would have started */
  on: false,
  loading: false,
  healOsc: null,

  /* 528 Hz solfeggio sine blended far beneath the song */
  startHealingTone() {
    if (this.healOsc) return;
    const osc = this.ctx.createOscillator();
    osc.type = "sine";
    osc.frequency.value = 528;
    const g = this.ctx.createGain();
    g.gain.value = 0.012;
    osc.connect(g);
    g.connect(this.ctx.destination);
    osc.start();
    this.healOsc = osc;
  },

  async ensureBuffer() {
    if (this.buffer || this.loading) return;
    this.loading = true;
    try {
      const data = await fetch(SONG_URL).then((r) => r.arrayBuffer());
      this.buffer = await this.ctx.decodeAudioData(data);
    } finally {
      this.loading = false;
    }
  },

  currentTime() {
    if (!this.buffer || !this.source) return 0;
    const t = this.ctx.currentTime - this.startedAt;
    return ((t % this.buffer.duration) + this.buffer.duration) % this.buffer.duration;
  },

  playAt(offset) {
    if (!this.buffer) return;
    if (this.source) {
      try { this.source.stop(); } catch {}
      this.source.disconnect();
    }
    const src = this.ctx.createBufferSource();
    src.buffer = this.buffer;
    src.loop = true;
    src.connect(this.gain);
    /* short fade-in avoids clicks on chapter jumps */
    const now = this.ctx.currentTime;
    this.gain.gain.cancelScheduledValues(now);
    this.gain.gain.setValueAtTime(0.0001, now);
    this.gain.gain.exponentialRampToValueAtTime(1, now + 0.08);
    src.start(0, offset % this.buffer.duration);
    this.source = src;
    this.startedAt = now - offset;
  },
};

window.__affirmPlayer = player; /* debug handle */

function chapterStart(seg) {
  if (!player.buffer) return 0;
  return (player.buffer.duration / N_QUOTES) * seg;
}

function seekToSegment(seg) {
  if (!player.on || !player.buffer) return;
  const start = chapterStart(seg);
  const len = player.buffer.duration / N_QUOTES;
  const t = player.currentTime();
  /* only jump when the playhead has left this affirmation's slice */
  if (t < start - 0.35 || t > start + len + 0.35) {
    player.playAt(start);
  }
}

soundToggle.addEventListener("click", async () => {
  player.on = !player.on;
  soundToggle.setAttribute("aria-pressed", String(player.on));
  soundLabel.textContent = player.on ? "SOUND ON" : "SOUND OFF";
  try {
    if (player.on) {
      if (!player.ctx) {
        player.ctx = new (window.AudioContext || window.webkitAudioContext)();
        player.gain = player.ctx.createGain();
        player.gain.connect(player.ctx.destination);
      }
      const buffering = player.ensureBuffer(); /* don't gate the fetch on resume() */
      await player.ctx.resume();
      await buffering;
      if (player.on) {
        player.startHealingTone();
        player.playAt(chapterStart(Math.max(activeSeg, 0)));
      }
    } else if (player.ctx) {
      await player.ctx.suspend();
    }
  } catch (e) {
    console.warn("Affirm: audio failed —", e.name, e.message);
    player.on = false;
    soundToggle.setAttribute("aria-pressed", "false");
    soundLabel.textContent = "SOUND OFF";
  }
});

