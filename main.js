/* ---------- frame preloading ---------- */

const frames = []; /* flat, global timeline */
const clipOffsets = [];
let totalFrames = 0;
for (const c of CLIPS) {
  clipOffsets.push(totalFrames);
  totalFrames += c.count;
}
frames.length = totalFrames;

let loadedCount = 0;
let firstClipReady = false;
const loaderEl = document.getElementById("scrub-loader");
const loaderPct = document.getElementById("loader-pct");

function frameUrl(clip, i) {
  return `${clip.dir}/f${String(i + 1).padStart(4, "0")}.webp`;
}

function loadClip(ci) {
  const clip = CLIPS[ci];
  const promises = [];
  for (let i = 0; i < clip.count; i++) {
    const gi = clipOffsets[ci] + i;
    promises.push(
      new Promise((res) => {
        const img = new Image();
        img.decoding = "async";
        img.onload = () => {
          frames[gi] = img;
          loadedCount++;
          if (!firstClipReady) {
            loaderPct.textContent = Math.round((loadedCount / CLIPS[0].count) * 100) + "%";
          }
          res();
        };
        img.onerror = res; /* keep last drawn frame on gaps */
        img.src = frameUrl(clip, i);
      })
    );
  }
  return Promise.all(promises);
}

(async () => {
  await loadClip(0);
  firstClipReady = true;
  loaderEl.style.display = "none";
  requestDraw();
  for (let ci = 1; ci < CLIPS.length; ci++) await loadClip(ci);
})();

/* ---------- canvas scrub ---------- */

const canvas = document.getElementById("hands-canvas");
const ctx = canvas.getContext("2d");
const scrubSection = document.querySelector(".scrub");
const quoteEl = document.getElementById("scrub-quote");
const numEl = document.getElementById("scrub-num");
const hintEl = document.getElementById("scrub-hint");

let current = 0; /* smoothed frame position */
let target = 0;
let drawnIndex = -1;
let rafPending = false;

function sizeCanvas() {
  const dpr = Math.min(devicePixelRatio || 1, 2);
  canvas.width = Math.round(canvas.clientWidth * dpr);
  canvas.height = Math.round(canvas.clientHeight * dpr);
  drawnIndex = -1;
  requestDraw();
}
addEventListener("resize", sizeCanvas);

function nearestLoaded(idx) {
  if (frames[idx]) return frames[idx];
  for (let d = 1; d < totalFrames; d++) {
    if (frames[idx - d]) return frames[idx - d];
    if (frames[idx + d]) return frames[idx + d];
  }
  return null;
}

function draw(idx) {
  const img = nearestLoaded(idx);
  if (!img) return;
  const dark = isDark();
  const paperRGB = dark ? "10,10,12" : "245,245,243";
  const cw = canvas.width, ch = canvas.height;
  ctx.filter = "none";
  ctx.fillStyle = `rgb(${paperRGB})`;
  ctx.fillRect(0, 0, cw, ch);
  /* contain-fit scaled down, anchored to the bottom edge — the cuffs stay
     connected to the frame bottom and the hands rise from below */
  const s = Math.min(cw / img.width, ch / img.height) * DRAW_SCALE;
  const w = img.width * s, h = img.height * s;
  const x = (cw - w) / 2, y = ch - h;
  if (dark) ctx.filter = "invert(1)"; /* negative stage: dark paper, dark gloves */
  ctx.drawImage(img, x, y, w, h);
  ctx.filter = "none";

  /* feather the image edges into the paper so the video background never
     reads as a rectangle */
  const F = Math.round(Math.min(w, h) * 0.18);
  const paper = (a) => `rgba(${paperRGB},${a})`;
  let g = ctx.createLinearGradient(0, y, 0, y + F);
  g.addColorStop(0, paper(1)); g.addColorStop(1, paper(0));
  ctx.fillStyle = g; ctx.fillRect(x - 1, y, w + 2, F);
  g = ctx.createLinearGradient(x, 0, x + F, 0);
  g.addColorStop(0, paper(1)); g.addColorStop(1, paper(0));
  ctx.fillStyle = g; ctx.fillRect(x, y - 1, F, h + 2);
  g = ctx.createLinearGradient(x + w, 0, x + w - F, 0);
  g.addColorStop(0, paper(1)); g.addColorStop(1, paper(0));
  ctx.fillStyle = g; ctx.fillRect(x + w - F, y - 1, F, h + 2);

  drawnIndex = idx;
}

/* ---------- quote reveal ---------- */

let activeSeg = -1;

function setSegment(seg) {
  if (seg === activeSeg) return;
  activeSeg = seg;
  numEl.textContent = String(seg + 1).padStart(2, "0");
  quoteEl.innerHTML = AFFIRMATIONS[seg]
    .split(" ")
    .map((w) => `<span class="w">${w}</span>`)
    .join(" ");
  seekToSegment(seg);
}

function updateWords(within) {
  const words = quoteEl.children;
  const n = words.length;
  /* all words revealed by 60% of the segment */
  const shown = Math.floor(Math.min(1, within / 0.6) * n + 0.0001);
  for (let i = 0; i < n; i++) words[i].classList.toggle("on", i < shown);
}

/* ---------- scroll wiring ---------- */

function onScroll() {
  const rect = scrubSection.getBoundingClientRect();
  const runway = rect.height - innerHeight;
  const p = Math.min(1, Math.max(0, -rect.top / runway));
  target = p * (totalFrames - 1);

  const segF = Math.min(0.999999, p) * N_QUOTES;
  const seg = Math.floor(segF);
  setSegment(seg);
  updateWords(segF - seg);

  hintEl.style.opacity = p > 0.005 ? "0" : "1";
  requestDraw();
}
addEventListener("scroll", onScroll, { passive: true });

/* rAF watcher: catches scroll-position changes that don't dispatch a scroll
   event (programmatic jumps, some mobile browsers during momentum) */
let lastY = -1;
(function watchScroll() {
  if (scrollY !== lastY) {
    lastY = scrollY;
    onScroll();
  }
  requestAnimationFrame(watchScroll);
})();

function requestDraw() {
  if (rafPending) return;
  rafPending = true;
  requestAnimationFrame(step);
}

function step() {
  rafPending = false;
  const ease = reducedMotion ? 1 : 0.16;
  current += (target - current) * ease;
  if (Math.abs(target - current) < 0.5) current = target;
  const idx = Math.round(current);
  if (idx !== drawnIndex) draw(idx);
  if (current !== target) requestDraw();
}

/* ---------- statement word reveal ---------- */

for (const el of document.querySelectorAll(".reveal-words")) {
  el.innerHTML = el.textContent
    .trim()
    .split(/\s+/)
    .map((w) => `<span class="w">${w}</span>`)
    .join(" ");
  const words = [...el.children];
  const io = new IntersectionObserver(
    (entries) => {
      if (!entries[0].isIntersecting) return;
      words.forEach((w, i) => setTimeout(() => w.classList.add("on"), reducedMotion ? 0 : i * 28));
      io.disconnect();
    },
    { threshold: 0.4 }
  );
  io.observe(el);
}

